// * imports
// *
// ****************************************************

const express = require("express");

// ****************************************************
// *
// * initialization
// *
// ****************************************************

const config = require("config");
const app = express();

const port = config.get("port");

// ****************************************************
// *
// * server config
// *
// ****************************************************

app.use(express.json());

// ****************************************************
// *
// * db connection
// *
// ****************************************************


// ****************************************************
// *
// * router declaration
// *
// ****************************************************

app.get("/", (req, res) => {
	res.send("Hello World!");
});

// ****************************************************
// *
// * server execution
// *
// ****************************************************

app.listen(port, () => {
	console.log(`App listening on port ${port}`);
});
